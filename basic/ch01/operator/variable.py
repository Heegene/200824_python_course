# variable.py

# 정수형(int)
i = 30
print('i = ', i)
print(type(i))

# 실수형(float)
d = 3.14
print("d = ", d)
print(type(d))

# 논리형(bool)
b1 = True
b2 = False

print("b1 = ", b1)
print("b2 = ", b2)
print(type(b1))
print(type(b2))

# 문자형(str)
str = 'Python'
print("str = ", str)
print(type(str))

# 리스트(list)
list = [10, "20", 30]
print('list = ', list)
print(type(list))

# 튜플(tuple)
t = (10, 20, 30)
print('t = ' , t)
print(type(t))

# 딕셔너리(dict)
d = {'a':10, 'b':"20", "c":30}
print(d)
print(type(d))